/* Array Metthods
- Single dimensional Array- Its an Array in which elements are stored in a single row
     E.g
*/
     let singleArray = ['IFT', 'MCM', 'TCS', 'CSC', 'LIS'];
     console.log(singleArray);

/* 
- Multi-dimensional Array- Its used for matrices like structure
     E.g
 */
     let multiArray = [
          [1, 2],
          [2, 3],
          [3, 4],
     ];
     console.log (multiArray);
/* 
- Dynamic Array- its an array that can shrink and grow during its runtime
     E.g
*/
     let dynamicArray = [];
     dynamicArray.push(1),
     dynamicArray.push(2),
     dynamicArray.push(3)
     console.log(dynamicArray);
/*
- Associative Array- it stores data in a key value frame
     E.g
*/
     let associativeArrayPerson ={
          name: 'Jack',
          age: 18,
     };
     console.log(associativeArrayPerson);
/* 
- Jagged Array- Its an array with different length
     E.g
*/
     let jaggedArray =[
          [1, 2],
          [1, 2, 3],
          [1]
     ];
     console.log(jaggedArray);
